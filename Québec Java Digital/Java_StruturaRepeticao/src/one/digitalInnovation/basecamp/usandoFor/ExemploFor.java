package one.digitalInnovation.basecamp.usandoFor;

public class ExemploFor {
    public static void main(String[] args) {
        for (int i = 1; i<=20; i++){
            System.out.println("++contador: "+ i);
        }

        System.out.println("\n");

        for(int i=20; i>=0; i--){
            System.out.println("--contador: "+ i);
        }
    }
}
